    def create_category(
        self,
        category: str,
        *,
        subcategory: str | None = None,
        description: str | None = None,
        actor: str = "system",
        in_outer_txn: bool = False,
    ) -> str:
        """Create a custom user category (active by default).

        Duplicate-text detection scans the full resolved taxonomy in
        ``core.dim_categories`` (seeds ∪ ``app.user_categories``), not just
        ``app.user_categories``, so a user cannot create a row whose
        ``(category, subcategory)`` text collides with a seeded default.
        Permitting such a collision would make ``resolve_category_id`` pick
        between two equally-matching dim rows arbitrarily, binding writer
        FKs to one or the other across calls. V015 relaxed
        ``app.user_categories``'s ``UNIQUE (category, subcategory)``
        constraint — the ``category_id`` PK is now the sole DB-enforced
        uniqueness contract; the cross-source check happens here.

        Raises:
            UserError(code="CATEGORY_ALREADY_EXISTS"): the
                ``(category, subcategory)`` pair is already present in
                ``core.dim_categories`` (either as a seeded default or a
                prior user-created row).
        """
        existing = self._db.execute(
            f"""
            SELECT 1 FROM {CATEGORIES.full_name}
            WHERE category = ? AND subcategory IS NOT DISTINCT FROM ?
            LIMIT 1
            """,  # noqa: S608  # TableRef constant, no user input interpolated
            [category, subcategory],
        ).fetchone()
        if existing:
            sub = f" / {subcategory}" if subcategory else ""
            raise UserError(
                f"Category already exists: {category}{sub}",
                code="CATEGORY_ALREADY_EXISTS",
            )

        # The duplicate check above and the insert below are separate statements
        # (the repo opens its own transaction). MoneyBin's single-writer model
        # (exclusive write lock per database-writer-coordination.md) is what makes
        # this race-free; V015 dropped the UNIQUE(category, subcategory) DB guard,
        # so the lock — not a constraint — is the load-bearing invariant here.
        event = self._user_categories.insert(
            category=category,
            subcategory=subcategory,
            description=description,
            actor=actor,
            in_outer_txn=in_outer_txn,
        )
        if event.target_id is None:  # pragma: no cover — insert always sets the id
            raise RuntimeError("UserCategoriesRepo.insert returned no category_id")
        return event.target_id

    def _complete_rows(
        self,
        table: TableRef,
        *,
        category_id: str,
        pk_columns: tuple[str, ...],
    ) -> tuple[CategoryReferenceSnapshot, ...]:
        """Read complete category references in deterministic primary-key order."""
        order_by = ", ".join(quote_ident(column) for column in pk_columns)
        cursor = self._db.execute(
            f"""
            SELECT *
            FROM {table.full_name}
            WHERE category_id = ?
            ORDER BY {order_by}
            """,  # noqa: S608  # TableRef + quoted code-owned identifiers
            [category_id],
        )
        columns = tuple(str(description[0]) for description in cursor.description)
        snapshots: list[CategoryReferenceSnapshot] = []
        for values in cursor.fetchall():
            row = dict(zip(columns, values, strict=True))
            key_values = tuple(row[column] for column in pk_columns)
            target_id = (
                str(key_values[0])
                if len(key_values) == 1
                else json.dumps(
                    key_values,
                    default=str,
                    ensure_ascii=False,
                    separators=(",", ":"),
                )
            )
            snapshots.append(
                CategoryReferenceSnapshot(
                    target_id=target_id,
                    before_state=row,
                )
            )
        return tuple(snapshots)

    def plan_category_delete(
        self,
        category_id: str,
        *,
        force: bool,
        excluded_references: frozenset[tuple[str, str]] = frozenset(),
    ) -> CategoryDeletePlan:
        """Snapshot and validate one category hard delete without writing."""
        category_cursor = self._db.execute(
            f"""
            SELECT *
            FROM {USER_CATEGORIES.full_name}
            WHERE category_id = ?
            """,  # noqa: S608  # TableRef constant
            [category_id],
        )
        columns = tuple(
            str(description[0]) for description in category_cursor.description
        )
        category_row = category_cursor.fetchone()
        if category_row is None:
            default = self._db.execute(
                f"""
                SELECT 1
                FROM {CATEGORIES.full_name}
                WHERE category_id = ? AND is_default = TRUE
                """,  # noqa: S608  # TableRef constant
                [category_id],
            ).fetchone()
            if default:
                raise UserError(
                    f"Default category {category_id} cannot be deleted "
                    "(use categories_set with is_active=False to disable)",
                    code="CATEGORY_IS_DEFAULT",
                )
            raise UserError(
                f"Category {category_id} not found",
                code="CATEGORY_NOT_FOUND",
            )

        references = tuple(
            CategoryReferenceGroup(
                store=table.full_name,
                rows=self._complete_rows(
                    table,
                    category_id=category_id,
                    pk_columns=pk_columns,
                ),
            )
            for table, pk_columns in (
                (TRANSACTION_CATEGORIES, ("transaction_id",)),
                (BUDGETS, ("budget_id",)),
                (USER_MERCHANTS, ("merchant_id",)),
                (TRANSACTION_SPLITS, ("split_id",)),
                (CATEGORIZATION_RULES, ("rule_id",)),
                (PROPOSED_RULES, ("proposed_rule_id",)),
                (
                    CATEGORY_SOURCE_MAP,
                    ("source_type", "source_category_code"),
                ),
            )
        )
        plan = CategoryDeletePlan(
            category_id=category_id,
            before_state=dict(zip(columns, category_row, strict=True)),
            references=references,
            excluded_references=excluded_references,
        )
        effective_usage = plan.usage(effective=True)
        if any(effective_usage.values()) and not force:
            present = [
                label.replace("_", " ")
                for label, count in effective_usage.items()
                if count
            ]
            raise UserError(
                f"Category {category_id} is referenced by "
                + ", ".join(present)
                + "; pass force=True to cascade-delete.",
                code="CATEGORY_HAS_REFERENCES",
                details={
                    "usage": plan.usage(),
                    "effective_usage": effective_usage,
                },
            )
        return plan

    def _apply_category_delete_plan(
        self,
        plan: CategoryDeletePlan,
        *,
        force: bool,
        actor: str,
    ) -> None:
        """Apply the shared seven-repository cascade inside an active transaction."""
        if force:
            self._tx_categories.delete_by_category(
                plan.category_id,
                actor=actor,
                in_outer_txn=True,
            )
            self._budgets.delete_by_category(
                plan.category_id,
                actor=actor,
                in_outer_txn=True,
            )
            self._user_merchants.delete_by_category(
                plan.category_id,
                actor=actor,
                in_outer_txn=True,
            )
            self._tx_splits.delete_by_category(
                plan.category_id,
                actor=actor,
                in_outer_txn=True,
            )
            self._rules.delete_by_category(
                plan.category_id,
                actor=actor,
                in_outer_txn=True,
            )
            self._proposed_rules.delete_by_category(
                plan.category_id,
                actor=actor,
                in_outer_txn=True,
            )
            self._category_source_map.delete_by_category(
                plan.category_id,
                actor=actor,
                in_outer_txn=True,
            )
        self._user_categories.delete(
            plan.category_id,
            actor=actor,
            in_outer_txn=True,
        )

    def plan_taxonomy_targets(
        self, targets: Sequence[TaxonomyStateTarget]
    ) -> TaxonomyTargetPlan:
        """Resolve the complete taxonomy batch before any write."""
        category_rows = self._db.execute(
            f"""
            SELECT category_id, category, subcategory, description,
                   is_default, is_active
            FROM {CATEGORIES.full_name}
            """  # noqa: S608  # TableRef constant
        ).fetchall()
        merchant_rows = self._db.execute(
            f"""
            SELECT merchant_id, raw_pattern, match_type, canonical_name,
                   category, subcategory, category_id, created_by,
                   exemplars, created_at, updated_at
            FROM {USER_MERCHANTS.full_name}
            """  # noqa: S608  # TableRef constant
        ).fetchall()
        category_by_id = {str(row[0]): row for row in category_rows}
        category_candidates_by_name: dict[
            tuple[str | None, str | None],
            list[tuple[object, ...]],
        ] = {}
        for row in category_rows:
            key = (
                _normalized_taxonomy_text(str(row[1])),
                _normalized_taxonomy_text(str(row[2]) if row[2] is not None else None),
            )
            category_candidates_by_name.setdefault(key, []).append(row)
        planned_categories = {
            (
                _normalized_taxonomy_text(target.category),
                _normalized_taxonomy_text(target.subcategory),
            )
            for target in targets
            if isinstance(target, CategoryStateTarget)
            and target.state == "present"
            and target.category is not None
        }
        removed_category_keys = {
            (
                _normalized_taxonomy_text(str(category_by_id[target.category_id][1])),
                _normalized_taxonomy_text(
                    str(category_by_id[target.category_id][2])
                    if category_by_id[target.category_id][2] is not None
                    else None
                ),
            )
            for target in targets
            if isinstance(target, CategoryStateTarget)
            and target.state == "absent"
            and target.category_id in category_by_id
        }
        merchant_by_id = {str(row[0]): row for row in merchant_rows}
        merchant_candidates_by_target: dict[
            tuple[str | None, object, str | None, str | None, str | None],
            list[tuple[object, ...]],
        ] = {}
        for row in merchant_rows:
            key = (
                _normalized_taxonomy_text(str(row[1]) if row[1] is not None else None),
                row[2],
                _normalized_taxonomy_text(str(row[3])),
                _normalized_taxonomy_text(str(row[4]) if row[4] is not None else None),
                _normalized_taxonomy_text(str(row[5]) if row[5] is not None else None),
            )
            merchant_candidates_by_target.setdefault(key, []).append(row)
        planned_merchant_delete_ids = {
            target.merchant_id
            for target in targets
            if isinstance(target, MerchantStateTarget)
            and target.state == "absent"
            and target.merchant_id in merchant_by_id
        }
        seen: set[tuple[str, object]] = set()
        items: list[TaxonomyTargetPlanItem] = []
        for target in targets:
            if isinstance(target, CategoryStateTarget):
                natural: tuple[str, str | None] | None = None
                if target.category is not None:
                    natural = (
                        cast(str, _normalized_taxonomy_text(target.category)),
                        _normalized_taxonomy_text(target.subcategory),
                    )
                if target.category_id is not None:
                    row = category_by_id.get(target.category_id)
                else:
                    category_matches = category_candidates_by_name.get(
                        cast(tuple[str | None, str | None], natural),
                        [],
                    )
                    if len(category_matches) > 1:
                        raise UserError(
                            "Category target matches more than one existing category.",
                            code=error_codes.MUTATION_AMBIGUOUS,
                            details={
                                "candidate_ids": sorted(
                                    str(candidate[0]) for candidate in category_matches
                                )
                            },
                        )
                    row = category_matches[0] if category_matches else None
                if (
                    target.category_id is not None
                    and row is None
                    and target.state != "absent"
                ):
                    raise UserError(
                        "Category target was not found.",
                        code=error_codes.MUTATION_NOT_FOUND,
                    )
                if (
                    target.category_id is not None
                    and row is not None
                    and target.state == "present"
                    and (
                        row[1] != target.category
                        or row[2] != target.subcategory
                        or (
                            target.description is not None
                            and row[3] != target.description
                        )
                    )
                ):
                    raise UserError(
                        "Explicit category ID does not match the requested target.",
                        code=error_codes.MUTATION_INVALID_INPUT,
                    )
                identity: tuple[str, object] = (
                    "category",
                    str(row[0]) if row is not None else cast(object, natural),
                )
                if identity in seen:
                    raise UserError(
                        "A category can appear only once in one taxonomy batch.",
                        code=error_codes.MUTATION_INVALID_INPUT,
                    )
                seen.add(identity)
                if target.state == "present":
                    action: Literal[
                        "create", "activate", "deactivate", "delete", "noop"
                    ] = (
                        "create"
                        if row is None
                        else "noop"
                        if bool(row[5])
                        else "activate"
                    )
                elif row is None:
                    action = "noop"
                elif target.state == "inactive":
                    action = "noop" if not bool(row[5]) else "deactivate"
                else:
                    action = "delete"
                usage: dict[str, int] = {}
                effective_usage: dict[str, int] = {}
                category_delete: CategoryDeletePlan | None = None
                if action == "delete" and row is not None:
                    excluded = frozenset(
                        ("app.user_merchants", merchant_id)
                        for merchant_id in planned_merchant_delete_ids
                        if merchant_by_id[merchant_id][6] == row[0]
                    )
                    category_delete = self.plan_category_delete(
                        str(row[0]),
                        force=target.force,
                        excluded_references=excluded,
                    )
                    usage = category_delete.usage()
                    effective_usage = category_delete.usage(effective=True)
                before: dict[str, object] | None = (
                    category_delete.before_state
                    if category_delete is not None
                    else (
                        dict(
                            zip(
                                (
                                    "category_id",
                                    "category",
                                    "subcategory",
                                    "description",
                                    "is_default",
                                    "is_active",
                                ),
                                row,
                                strict=True,
                            )
                        )
                        if row is not None
                        else None
                    )
                )
                items.append(
                    TaxonomyTargetPlanItem(
                        target=target,
                        target_id=str(row[0]) if row is not None else None,
                        action=action,
                        before_state=before,
                        usage=usage,
                        effective_usage=effective_usage,
                        category_delete=category_delete,
                    )
                )
                continue

            match_type = target.match_type or "contains"
            if target.category is not None:
                category_key = (
                    _normalized_taxonomy_text(target.category),
                    _normalized_taxonomy_text(target.subcategory),
                )
                if category_key in removed_category_keys:
                    raise UserError(
                        "A merchant cannot target a category removed by the "
                        "same taxonomy batch.",
                        code=error_codes.MUTATION_INVALID_INPUT,
                    )
                category_matches = category_candidates_by_name.get(category_key, [])
                if len(category_matches) > 1:
                    raise UserError(
                        "Merchant category matches more than one existing category.",
                        code=error_codes.MUTATION_AMBIGUOUS,
                        details={
                            "candidate_ids": sorted(
                                str(candidate[0]) for candidate in category_matches
                            )
                        },
                    )
                if not category_matches and category_key not in planned_categories:
                    raise UserError(
                        "Merchant mapping references a category that was not found.",
                        code=error_codes.MUTATION_NOT_FOUND,
                    )
            natural_merchant = (
                _normalized_taxonomy_text(target.raw_pattern),
                match_type,
                _normalized_taxonomy_text(target.canonical_name),
                _normalized_taxonomy_text(target.category),
                _normalized_taxonomy_text(target.subcategory),
            )
            if target.merchant_id is not None:
                row = merchant_by_id.get(target.merchant_id)
            else:
                merchant_matches = merchant_candidates_by_target.get(
                    natural_merchant,
                    [],
                )
                if len(merchant_matches) > 1:
                    raise UserError(
                        "Merchant target matches more than one existing mapping.",
                        code=error_codes.MUTATION_AMBIGUOUS,
                        details={
                            "candidate_ids": sorted(
                                str(candidate[0]) for candidate in merchant_matches
                            )
                        },
                    )
                row = merchant_matches[0] if merchant_matches else None
            if (
                target.merchant_id is not None
                and row is None
                and target.state == "present"
            ):
                raise UserError(
                    "Merchant mapping target was not found.",
                    code=error_codes.MUTATION_NOT_FOUND,
                )
            if (
                target.merchant_id is not None
                and row is not None
                and target.state == "present"
                and (
                    row[1] != target.raw_pattern
                    or row[2] != match_type
                    or row[3] != target.canonical_name
                    or row[4] != target.category
                    or row[5] != target.subcategory
                )
            ):
                raise UserError(
                    "Explicit merchant ID does not match the requested target.",
                    code=error_codes.MUTATION_INVALID_INPUT,
                )
            identity = (
                "merchant",
                str(row[0]) if row is not None else natural_merchant,
            )
            if identity in seen:
                raise UserError(
                    "A merchant mapping can appear only once in one taxonomy batch.",
                    code=error_codes.MUTATION_INVALID_INPUT,
                )
            seen.add(identity)
            action = (
                "create"
                if target.state == "present" and row is None
                else "delete"
                if target.state == "absent" and row is not None
                else "noop"
            )
            before = cast(
                "dict[str, object] | None",
                (
                    dict(
                        zip(
                            (
                                "merchant_id",
                                "raw_pattern",
                                "match_type",
                                "canonical_name",
                                "category",
                                "subcategory",
                                "category_id",
                                "created_by",
                                "exemplars",
                                "created_at",
                                "updated_at",
                            ),
                            row,
                            strict=True,
                        )
                    )
                    if row is not None
                    else None
                ),
            )
            items.append(
                TaxonomyTargetPlanItem(
                    target=target,
                    target_id=str(row[0]) if row is not None else None,
                    action=action,
                    before_state=before,
                    usage={},
                    effective_usage={},
                )
            )
        return TaxonomyTargetPlan(items=tuple(items))

    def apply_taxonomy_targets(
        self,
        targets: Sequence[TaxonomyStateTarget],
        *,
        actor: str,
        verify: Callable[[TaxonomyTargetPlan], None] | None = None,
    ) -> list[TaxonomyTargetResult]:
        """Re-preflight and apply one taxonomy batch atomically."""
        self._db.begin()
        try:
            plan = self.plan_taxonomy_targets(targets)
            if verify is not None:
                verify(plan)
            if not plan.changed:
                raise UserError(
                    "Every taxonomy target already has its requested state.",
                    code=error_codes.MUTATION_NOTHING_TO_DO,
                )
            target_ids = [item.target_id for item in plan.items]
            for index, item in enumerate(plan.items):
                target = item.target
                if item.action == "create" and isinstance(target, CategoryStateTarget):
                    target_ids[index] = self.create_category(
                        cast(str, target.category),
                        subcategory=target.subcategory,
                        description=target.description,
                        actor=actor,
                        in_outer_txn=True,
                    )
                elif item.action in {"activate", "deactivate"}:
                    self.toggle_category(
                        cast(str, item.target_id),
                        is_active=item.action == "activate",
                        actor=actor,
                        in_outer_txn=True,
                    )
            for index, item in enumerate(plan.items):
                target = item.target
                if item.action == "create" and isinstance(target, MerchantStateTarget):
                    target_ids[index] = self.create_merchant_core(
                        target.raw_pattern,
                        cast(str, target.canonical_name),
                        match_type=cast(
                            InternalMatchType,
                            target.match_type or "contains",
                        ),
                        category=target.category,
                        subcategory=target.subcategory,
                        created_by="ai",
                        actor=actor,
                        in_outer_txn=True,
                    )
                elif item.action == "delete" and isinstance(
                    target, MerchantStateTarget
                ):
                    self._user_merchants.delete(
                        cast(str, item.target_id),
                        actor=actor,
                        in_outer_txn=True,
                    )
            for item in plan.items:
                target = item.target
                if item.action != "delete" or not isinstance(
                    target, CategoryStateTarget
                ):
                    continue
                if item.category_delete is None:  # pragma: no cover — planned delete
                    raise RuntimeError("Category delete is missing its preflight plan")
                self._apply_category_delete_plan(
                    item.category_delete,
                    force=target.force,
                    actor=actor,
                )
            self._db.commit()
            return [
                TaxonomyTargetResult(
                    kind=item.kind,
                    target_id=target_ids[index],
                    state=item.target.state,
                    changed=item.action != "noop",
                )
                for index, item in enumerate(plan.items)
            ]
        except BaseException:
            self._db.rollback()
            raise

    def toggle_category(
        self,
        category_id: str,
        *,
        is_active: bool,
        actor: str = "system",
        in_outer_txn: bool = False,
    ) -> None:
        """Enable or disable a category. Existing categorizations are preserved.

        Default categories (is_default=true) write to ``app.category_overrides``;
        user-created categories update ``app.user_categories.is_active`` directly.
        Both writes route through their repository so each emits a paired audit
        row (Invariant 10).

        Raises:
            UserError(code="CATEGORY_NOT_FOUND"): no category with this ID
                exists in either ``app.user_categories`` or the seeded defaults.
        """
        cat = self._db.execute(
            f"SELECT is_default FROM {CATEGORIES.full_name} WHERE category_id = ?",  # noqa: S608  # TableRef constant
            [category_id],
        ).fetchone()
        if not cat:
            raise UserError(
                f"Category {category_id} not found",
                code="CATEGORY_NOT_FOUND",
            )

        if cat[0]:  # default category — record/upsert the override
            self._category_overrides.set_active(
                category_id, is_active=is_active, actor=actor, in_outer_txn=in_outer_txn
            )
        else:
            self._user_categories.update_active(
                category_id, is_active=is_active, actor=actor, in_outer_txn=in_outer_txn
            )

    def delete_category(
        self, category_id: str, *, force: bool = False, actor: str = "system"
    ) -> None:
        """Hard-delete a user-created category.

        Reference check and cascade both use ``category_id`` FK matching across
        seven tables: ``app.transaction_categories``, ``app.budgets``,
        ``app.user_merchants``, ``app.transaction_splits``,
        ``app.categorization_rules``, ``app.proposed_rules``, and
        ``app.category_source_map``. With ``force=False`` (default) the call
        refuses when any of those tables carries a row with this
        ``category_id``; with ``force=True`` rows in all seven tables are
        deleted before the ``app.user_categories`` row itself is removed.

        Default (seeded) categories cannot be hard-deleted — operators disable
        them via :meth:`toggle_category` so that ``app.category_overrides``
        records the deactivation without destroying the canonical taxonomy row.

        Args:
            category_id: ID of the user-created category to delete.
            force: If True, cascade-delete referencing rows across the seven
                tables above; if False, refuse when references exist.
            actor: Audit actor recorded on the ``user_category.delete`` event
                (e.g. ``"cli"``, ``"mcp"``).

        Raises:
            UserError(code="CATEGORY_NOT_FOUND"): no category with this ID.
            UserError(code="CATEGORY_IS_DEFAULT"): seeded default categories
                cannot be hard-deleted; use ``toggle_category``.
            UserError(code="CATEGORY_HAS_REFERENCES"): force=False and the
                category is referenced by at least one of the seven tracked
                tables.
        """
        with self._transaction():
            plan = self.plan_category_delete(category_id, force=force)
            self._apply_category_delete_plan(plan, force=force, actor=actor)

    # -- Guarded categorization write --

    def write_categorization(
        self,
        *,
        transaction_id: str,
        category: str,
        subcategory: str | None,
        categorized_by: str,
        merchant_id: str | None = None,
        rule_id: str | None = None,
        confidence: float | None = None,
        source_type: str = "internal",
        in_outer_txn: bool = False,
    ) -> WriteOutcome:
        """Insert or replace a categorization, respecting source precedence.

        Single guarded write path for ``app.transaction_categories``. Lower
        numeric priority = higher authority (per
        ``categorization-matching-mechanics.md`` §Source precedence). A new
        write succeeds only if its source priority is ≤ the existing row's;
        otherwise the existing row stands and the
        ``CATEGORIZE_WRITE_SKIPPED_PRECEDENCE_TOTAL`` metric is incremented.

        Returns:
            ``WriteOutcome.written=True`` if the write took effect (insert or
            permitted update); ``False`` with ``skipped_reason='lower_priority_source'``
            if a higher-priority categorization already exists.

        Raises:
            ValueError: if ``categorized_by`` is not in :data:`SOURCE_PRIORITY`.
                The generated SQL CASE has no ELSE branch (see
                :func:`priority_case_sql`), so an unknown source would silently
                resolve to NULL priority and never overwrite — fail loudly
                instead of letting a typo masquerade as a precedence skip.
        """
        if categorized_by not in SOURCE_PRIORITY:
            raise ValueError(
                f"Unknown categorized_by={categorized_by!r}; "
                f"must be one of {sorted(SOURCE_PRIORITY)}"
            )
        # FK is paired with text on identical precedence terms. The repo owns
        # the precedence-guarded upsert + paired audit; a precedence-skipped
        # write returns None (no mutation, no audit). Engine writes record their
        # source via categorized_by, so the audit actor is the batch ("system").
        category_id = resolve_category_id(self._db, category, subcategory)
        event = self._tx_categories.upsert_guarded(
            transaction_id,
            category=category,
            subcategory=subcategory,
            category_id=category_id,
            categorized_by=categorized_by,
            merchant_id=merchant_id,
            rule_id=rule_id,
            confidence=confidence,
            source_type=source_type,
            actor="system",
            in_outer_txn=in_outer_txn,
        )
        if event is not None:
            return WriteOutcome(written=True)

        # Row exists with a higher-priority source; record the skip with labels
        # for both sides of the comparison so dashboards can distinguish "ai
        # blocked by user" from "ai blocked by rule" etc.
        existing = self._db.execute(
            f"SELECT categorized_by FROM {TRANSACTION_CATEGORIES.full_name} "
            "WHERE transaction_id = ?",  # noqa: S608  # TRANSACTION_CATEGORIES is a TableRef constant
            [transaction_id],
        ).fetchone()
        CATEGORIZE_WRITE_SKIPPED_PRECEDENCE_TOTAL.labels(
            src_existing=existing[0] if existing else "unknown",
            src_attempted=categorized_by,
        ).inc()
        return WriteOutcome(written=False, skipped_reason="lower_priority_source")
