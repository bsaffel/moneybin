class MatchApplier:
    """All writes to ``app.*`` categorization tables.

    Holds a ``Database`` handle plus an injected ``AuditService`` so the
    user-manual-edit path can emit ``category.set`` / ``category.clear``
    events alongside the underlying mutation.

    The ``*_core`` methods (``create_merchant_core``, ``create_rules_core``,
    ``deactivate_rule_core``) deliberately omit the optional ``reapply``
    semantics: the facade pairs the write with ``categorize_pending`` when
    callers ask for it. Splitting the orchestration this way keeps applier
    methods pure-write and lets the facade own the cross-cluster choreography.
    """

    def __init__(self, db: Database, *, audit: AuditService) -> None:
        """Bind the applier to a database connection and audit service."""
        self._db = db
        self._audit = audit
        # Protected app.* taxonomy writes route through repositories so each
        # mutation emits a paired audit row (Invariant 10). Repos share this
        # applier's AuditService + connection so their writes participate in
        # the same transaction when composed inside one.
        self._user_categories = UserCategoriesRepo(db, audit=audit)
        self._category_overrides = CategoryOverridesRepo(db, audit=audit)
        self._user_merchants = UserMerchantsRepo(db, audit=audit)
        self._budgets = BudgetsRepo(db, audit=audit)
        self._category_source_map = CategorySourceMapRepo(db, audit=audit)
        self._proposed_rules = ProposedRulesRepo(db, audit=audit)
        self._rules = CategorizationRulesRepo(db, audit=audit)
        self._tx_categories = TransactionCategoriesRepo(db, audit=audit)
        self._tx_splits = TransactionSplitsRepo(db, audit=audit)

    @contextmanager
    def _transaction(self) -> Generator[None]:
        """Run the wrapped block inside a DB transaction; rollback on failure.

        Rolls back on BaseException (incl. KeyboardInterrupt/SystemExit) so an
        interrupt mid-write — e.g. between repository-owned category cascade
        mutations — can't leave the transaction open. Same pattern as
        BaseRepo._transaction; re-raised immediately, never swallowed.
        """
        self._db.begin()
        try:
            yield
        except BaseException:
            self._db.rollback()
            raise
        else:
            self._db.commit()

    # -- Per-transaction category writes (audit emission) --

    def set_category(
        self,
        transaction_id: str,
        *,
        category: str,
        subcategory: str | None = None,
        categorized_by: Literal["user"] = "user",
        actor: str,
    ) -> None:
        """Upsert a transaction's user category and emit ``category.set`` audit.

        Captures the prior row (or NULL) as ``before`` and the new shape as
        ``after`` so the audit trail can reconstruct overwrites. Mutation +
        audit row commit atomically.
        """
        with self._transaction():
            self.set_category_in_active_txn(
                transaction_id,
                category=category,
                subcategory=subcategory,
                categorized_by=categorized_by,
                actor=actor,
            )

    def set_category_in_active_txn(
        self,
        transaction_id: str,
        *,
        category: str,
        subcategory: str | None,
        categorized_by: Literal["user"] = "user",
        actor: str,
    ) -> None:
        """``set_category`` body without txn boundaries.

        Use when the caller already owns a transaction and wants to batch
        multiple category writes atomically with their own audit chain.

        ``categorized_by`` is restricted to ``"user"``. The user-manual-edit
        path is the only intended use; the direct ``INSERT ... ON CONFLICT``
        below bypasses :meth:`write_categorization`'s precedence guard, so
        admitting a lower-priority source here would let it silently overwrite
        a higher-priority existing categorization. New callers needing a
        non-user write must route through :meth:`write_categorization`.
        """
        category_id = resolve_category_id(self._db, category, subcategory)
        # Routes through the repo (paired audit, full before/after — Req 4).
        # in_outer_txn=True: the caller already owns the transaction.
        self._tx_categories.set(
            transaction_id,
            category=category,
            subcategory=subcategory,
            category_id=category_id,
            categorized_by=categorized_by,
            actor=actor,
            in_outer_txn=True,
        )

    def clear_category(self, transaction_id: str, *, actor: str) -> None:
        """Delete a transaction's category row and emit ``category.clear`` audit.

        Routes through the repo (full before-row capture, Req 4). No-op (and no
        audit event) when no row exists.
        """
        self._tx_categories.clear(transaction_id, actor=actor)

    # -- Merchant management --

    def create_merchant_core(
        self,
        raw_pattern: str | None,
        canonical_name: str,
        *,
        match_type: InternalMatchType = "oneOf",
        category: str | None = None,
        subcategory: str | None = None,
        created_by: str = "ai",
        exemplars: list[str] | None = None,
        actor: str = "system",
        in_outer_txn: bool = False,
    ) -> str:
        """Insert a merchant mapping and return its ``merchant_id``.

        Routes the INSERT through :class:`UserMerchantsRepo` so it emits a
        paired ``app.audit_log`` row (Invariant 10). The facade's
        ``create_merchant`` wraps this with the optional ``reapply``
        post-commit sweep.

        Args:
            raw_pattern: Pattern to match in transaction descriptions; pass
                ``None`` for exemplar-only merchants (``match_type='oneOf'``).
            canonical_name: Clean merchant name for display.
            match_type: How to match: 'exact', 'contains', 'regex', or 'oneOf'.
                Defaults to ``'oneOf'`` — system-created merchants use the
                exemplar accumulator (categorization-matching-mechanics.md
                §Schema changes); user-authored merchants pick 'contains' or
                'regex' explicitly.
            category: Optional default category for this merchant.
            subcategory: Optional default subcategory.
            created_by: Who created the mapping ('user', 'ai', 'rule').
            exemplars: Initial exemplar set (exact match_text values) for
                oneOf merchants. Defaults to ``[]``.
            actor: Audit actor recorded on the ``user_merchant.insert`` event
                (e.g. ``"cli"``, ``"mcp"``); batch-path callers use the
                ``"system"`` default.
            in_outer_txn: Join the caller's active transaction when true.
        """
        # Resolve the FK alongside the text snapshot (read; stays in the
        # service per Req 2). The repo owns the INSERT + audit.
        category_id = resolve_category_id(self._db, category, subcategory)
        event = self._user_merchants.insert(
            raw_pattern=raw_pattern,
            match_type=match_type,
            canonical_name=canonical_name,
            category=category,
            subcategory=subcategory,
            category_id=category_id,
            created_by=created_by,
            exemplars=exemplars,
            actor=actor,
            in_outer_txn=in_outer_txn,
        )
        merchant_id = event.target_id
        if merchant_id is None:  # pragma: no cover — insert always sets the id
            raise RuntimeError("UserMerchantsRepo.insert returned no merchant_id")
        if exemplars and not in_outer_txn:
            MERCHANT_EXEMPLAR_COUNT.labels(merchant_id=merchant_id).set(len(exemplars))
        if not in_outer_txn:
            logger.info(f"Created user merchant {merchant_id}")
        return merchant_id

    def find_merchant_by_canonical_name(
        self,
        canonical_name: str,
        *,
        category: str,
        subcategory: str | None,
    ) -> str | None:
        """Return ``merchant_id`` for the oneOf merchant with this canonical name + category, or None.

        Used by the exemplar accumulator to detect whether a previously-created
        merchant should grow its exemplar set instead of spawning a duplicate.

        Filters on ``match_type='oneOf'`` because exemplars are only consulted
        for oneOf merchants (see :func:`matcher._match_exemplar`); appending an
        exemplar to a contains/exact/regex merchant is dead data. Also filters
        on ``category``/``subcategory``: two ``oneOf`` merchants can legitimately
        share a canonical name (e.g. an Amazon merchant mapped to ``Shopping``
        and another mapped to ``Groceries``); merging exemplars across them
        would cross-pollinate categorization. When the canonical name collides
        with a different-category merchant, the caller creates a new oneOf
        merchant alongside it — the two coexist, each doing its own job.
        """
        try:
            # IS NOT DISTINCT FROM treats NULL = NULL as true, so both
            # exemplar-set-only merchants (subcategory IS NULL) and ones with
            # a specific subcategory match on a single parameterized query.
            row = self._db.execute(
                f"SELECT merchant_id FROM {USER_MERCHANTS.full_name} "
                "WHERE canonical_name = ? AND match_type = 'oneOf' "
                "AND category = ? AND subcategory IS NOT DISTINCT FROM ? "
                "LIMIT 1",  # noqa: S608  # TableRef constant
                [canonical_name, category, subcategory],
            ).fetchone()
        except duckdb.CatalogException:
            return None
        return row[0] if row else None

    def append_exemplar(
        self,
        merchant_id: str,
        match_text: str,
        *,
        actor: str = "system",
        in_outer_txn: bool = False,
    ) -> int:
        """Append ``match_text`` to a merchant's exemplar set; return new size.

        Routes the UPDATE through :class:`UserMerchantsRepo` so it emits a
        paired audit row (Invariant 10). Idempotent via ``list_distinct``:
        re-appending an existing exemplar leaves the set unchanged and does not
        advance ``updated_at`` (docs/specs/core-updated-at-convention.md). The
        new size comes from the audit event's full after-row, so no extra read
        is needed. Updates the per-merchant gauge to surface any merchant whose
        set is approaching the soft-cap signal (200).
        """
        event = self._user_merchants.append_exemplar(
            merchant_id,
            match_text,
            actor=actor,
            in_outer_txn=in_outer_txn,
        )
        exemplars = cast("list[str]", (event.after_value or {}).get("exemplars") or [])
        new_size = len(exemplars)
        if not in_outer_txn:
            MERCHANT_EXEMPLAR_COUNT.labels(merchant_id=merchant_id).set(new_size)
        return new_size

    def record_committed_review_merchants(
        self,
        *,
        created_merchant_ids: tuple[str, ...],
        touched_merchant_ids: tuple[str, ...],
    ) -> None:
        """Publish merchant observability only after the outer transaction commits."""
        for merchant_id in dict.fromkeys(touched_merchant_ids):
            row = self._db.execute(
                f"""
                SELECT len(exemplars)
                FROM {USER_MERCHANTS.full_name}
                WHERE merchant_id = ?
                """,  # noqa: S608  # TableRef constant + parameterized value
                [merchant_id],
            ).fetchone()
            if row is not None:
                MERCHANT_EXEMPLAR_COUNT.labels(merchant_id=merchant_id).set(int(row[0]))
        for merchant_id in dict.fromkeys(created_merchant_ids):
            logger.info(f"Created user merchant {merchant_id}")

    # -- Rule management --

    def create_rules_core(
        self,
        items: Sequence[CategorizationRuleInput],
        *,
        actor: str = "system",
        allow_broad: bool = False,
    ) -> RuleCreationResult:
        """Insert categorization rules (idempotent on matcher + output shape).

        Each INSERT routes through :class:`CategorizationRulesRepo` so it emits
        a paired audit row (Invariant 10). The facade's ``create_rules``
        handles the optional ``reapply`` post-commit sweep.

        For each item, looks up an active rule with the same matcher and
        output (``merchant_pattern``, ``match_type``, ``min_amount``,
        ``max_amount``, ``account_id``, ``category``, ``subcategory``);
        ``name`` and ``priority`` are treated as metadata and excluded
        from the dedup key. If found, returns the existing ``rule_id``
        and bumps the ``existing`` counter; otherwise INSERTs a fresh
        12-char UUID hex ``rule_id`` with ``is_active=true`` and
        ``created_by='ai'``.

        Same matcher with a *different* category output is currently
        treated as a new rule, not a conflict — see
        ``docs/specs/moneybin-mcp.md`` "Rule-conflict detection
        (follow-up)" for the deferred conflict-resolution work.

        Per-row insertion failures are caught so a single bad row does
        not abort the batch — they appear in ``error_details``.

        Direct rule creation has no accumulated evidence (no trigger count),
        so the auto-rule "broad" test (blast radius vs. evidence) does not
        apply here — a legitimate ``contains "AMAZON"`` rule matching 200
        transactions would be flagged for no reason. What *does* transfer is
        the specificity floor: a ``contains`` pattern too short to
        discriminate (``is_unselective_contains``, shared with the auto-rule
        proposer's ``_invented_match_type`` guard) is refused unless the
        caller passes ``allow_broad=True`` — an agent must not be able to
        promote a ledger-wrecking rule just by calling this tool directly.
        """
        created = 0
        existing = 0
        skipped = 0
        error_details: list[dict[str, str]] = []
        rule_ids: list[str] = []

        for item in items:
            try:
                # DuckDB IS NOT DISTINCT FROM treats NULL = NULL as true,
                # so optional fields (min/max_amount, account_id, subcategory)
                # match on NULL across calls.
                found = self._db.execute(
                    f"""
                    SELECT rule_id FROM {CATEGORIZATION_RULES.full_name}
                    WHERE is_active = true
                      AND merchant_pattern = ?
                      AND match_type = ?
                      AND min_amount IS NOT DISTINCT FROM ?
                      AND max_amount IS NOT DISTINCT FROM ?
                      AND account_id IS NOT DISTINCT FROM ?
                      AND category = ?
                      AND subcategory IS NOT DISTINCT FROM ?
                    LIMIT 1
                    """,  # noqa: S608  # TableRef constant, no user input interpolated
                    [
                        item.merchant_pattern,
                        item.match_type,
                        item.min_amount,
                        item.max_amount,
                        item.account_id,
                        item.category,
                        item.subcategory,
                    ],
                ).fetchone()
                if found is not None:
                    existing += 1
                    rule_ids.append(found[0])
                    continue
                # The specificity gate only guards the INSERT path: an
                # already-active rule with this exact matcher+output found
                # above short-circuits to `existing` before we ever get
                # here, so re-submitting a previously allow_broad'd (or
                # pre-guard) rule stays idempotent instead of being refused
                # (docs/specs/moneybin-mcp.md "Idempotent" contract).
                if not allow_broad and is_unselective_contains(
                    item.merchant_pattern, item.match_type
                ):
                    RULE_CREATE_UNSELECTIVE_CONTAINS_BLOCKED_TOTAL.inc()
                    skipped += 1
                    error_details.append({
                        "name": item.name,
                        "reason": (
                            f"Pattern {item.merchant_pattern!r} is too short to be a "
                            "'contains' rule — it would match unrelated merchants "
                            "(e.g. a 2-char pattern like 'TO' matches STORE, AUTO, "
                            "TOTAL). Use match_type='exact' for a short pattern, or "
                            "re-run with allow_broad=True to accept the risk."
                        ),
                    })
                    continue
                # Phase 1 dual-write: resolve the FK alongside the text snapshot
                # (read; stays in the service per Req 2). `None` is a permitted
                # write — orphaned text is a real state during the dual-write
                # window. Dedup posture is unchanged: text remains the canonical
                # comparator for "same rule" in Phase 1.
                category_id = resolve_category_id(
                    self._db, item.category, item.subcategory
                )
                event = self._rules.insert(
                    name=item.name,
                    merchant_pattern=item.merchant_pattern,
                    match_type=item.match_type,
                    min_amount=item.min_amount,
                    max_amount=item.max_amount,
                    account_id=item.account_id,
                    category=item.category,
                    subcategory=item.subcategory,
                    category_id=category_id,
                    priority=item.priority,
                    created_by="ai",
                    actor=actor,
                )
                rule_id = event.target_id
                if rule_id is None:  # pragma: no cover — insert always sets the id
                    raise RuntimeError("CategorizationRulesRepo.insert returned no id")
                created += 1
                rule_ids.append(rule_id)
            except Exception:  # noqa: BLE001 — DuckDB raises untyped errors on constraint violations
                skipped += 1
                logger.exception(f"create_rules failed for rule {item.name!r}")
                error_details.append({
                    "name": item.name,
                    "reason": "Failed to create rule — check logs for details.",
                })

        return RuleCreationResult(
            created=created,
            existing=existing,
            skipped=skipped,
            error_details=error_details,
            rule_ids=rule_ids,
        )

    def deactivate_rule_core(self, rule_id: str, *, actor: str = "system") -> bool:
        """Soft-delete a rule by setting ``is_active=false``.

        Routes through :class:`CategorizationRulesRepo` so the deactivation
        emits a paired audit row (Invariant 10). Returns ``True`` if the rule
        existed (and is now inactive), ``False`` if no rule with that ID was
        found. The facade's ``deactivate_rule`` handles the optional reapply
        tail (categorization stripping + ``categorize_pending``).
        """
        return self._rules.deactivate(rule_id, actor=actor) is not None

    def plan_rule_targets(
        self,
        targets: Sequence[RuleStateTarget],
    ) -> RuleTargetPlan:
        """Validate and resolve every target before a rule batch can write."""
        rows = self._db.execute(
            f"""
            SELECT rule_id, name, merchant_pattern, match_type, min_amount,
                   max_amount, account_id, category, subcategory, priority, is_active,
                   category_id, created_by, created_at, updated_at
            FROM {CATEGORIZATION_RULES.full_name}
            """  # noqa: S608  # TableRef constant
        ).fetchall()
        by_id = {str(row[0]): row for row in rows}
        candidates = [
            EntityCandidate(entity_id=str(row[0]), display_name=str(row[1]))
            for row in rows
        ]
        resolved_seen: set[str] = set()
        natural_seen: set[tuple[object, ...]] = set()
        items: list[RuleTargetPlanItem] = []
        for target in targets:
            if target.state == "present":
                natural_key = self._rule_target_natural_key(target)
                if natural_key in natural_seen:
                    raise UserError(
                        "A logical rule target can appear only once in one batch.",
                        code=error_codes.MUTATION_INVALID_INPUT,
                    )
                natural_seen.add(natural_key)
            row = self._resolve_rule_target_row(target, by_id, candidates)
            rule_id = str(row[0]) if row is not None else None
            if rule_id is not None:
                if rule_id in resolved_seen:
                    raise UserError(
                        "A rule can appear only once in one target-state batch.",
                        code=error_codes.MUTATION_INVALID_INPUT,
                    )
                resolved_seen.add(rule_id)
            action = self._rule_target_action(target, row)
            if (
                action in {"create", "set"}
                and target.merchant_pattern is not None
                and target.match_type is not None
                and is_unselective_contains(
                    target.merchant_pattern,
                    target.match_type,
                )
            ):
                RULE_CREATE_UNSELECTIVE_CONTAINS_BLOCKED_TOTAL.inc()
                raise UserError(
                    "Contains rule patterns must be long enough to discriminate.",
                    code=error_codes.MUTATION_INVALID_INPUT,
                )
            items.append(
                RuleTargetPlanItem(
                    target,
                    rule_id,
                    action,
                    self._rule_row_digest(row),
                )
            )
        return RuleTargetPlan(items=tuple(items))

    @staticmethod
    def _rule_target_natural_key(target: RuleStateTarget) -> tuple[object, ...]:
        """Return the logical identity used by natural-key resolution."""
        return (
            target.merchant_pattern,
            target.match_type,
            target.min_amount,
            target.max_amount,
            target.account_id,
            target.category,
            target.subcategory,
        )

    @staticmethod
    def _rule_row_digest(row: tuple[object, ...] | None) -> str | None:
        """Bind confirmation to the complete current row without exposing it."""
        if row is None:
            return None
        canonical = json.dumps(
            row,
            default=str,
            ensure_ascii=False,
            separators=(",", ":"),
        ).encode()
        return hashlib.sha256(canonical).hexdigest()

    @staticmethod
    def _resolve_rule_target_row(
        target: RuleStateTarget,
        by_id: dict[str, tuple[object, ...]],
        candidates: list[EntityCandidate],
    ) -> tuple[object, ...] | None:
        """Resolve an explicit reference or a present target's natural key."""
        if target.rule_id is not None:
            resolved = resolve_entity_reference(target.rule_id, candidates)
            if isinstance(resolved, AmbiguousEntity):
                raise UserError(
                    "Categorization rule reference is ambiguous.",
                    code=error_codes.MUTATION_INVALID_INPUT,
                    details={"candidate_ids": list(resolved.candidate_ids)},
                )
            if isinstance(resolved, MissingEntity):
                if target.state == "absent":
                    return None
                raise UserError(
                    "Categorization rule was not found.",
                    code=error_codes.MUTATION_NOT_FOUND,
                    details={"reference": target.rule_id},
                )
            return by_id[resolved.entity_id]
        if target.state != "present":  # guarded by the MCP contract
            raise UserError(
                f"state='{target.state}' requires rule_id.",
                code=error_codes.MUTATION_INVALID_INPUT,
            )
        matches = [
            row
            for row in by_id.values()
            if row[2] == target.merchant_pattern
            and row[3] == target.match_type
            and row[4] == target.min_amount
            and row[5] == target.max_amount
            and row[6] == target.account_id
            and row[7] == target.category
            and row[8] == target.subcategory
        ]
        if len(matches) > 1:
            raise UserError(
                "Categorization rule target matches more than one existing rule.",
                code=error_codes.MUTATION_INVALID_INPUT,
                details={"candidate_ids": sorted(str(row[0]) for row in matches)},
            )
        return matches[0] if matches else None

    @staticmethod
    def _rule_target_action(
        target: RuleStateTarget,
        row: tuple[object, ...] | None,
    ) -> Literal["create", "set", "deactivate", "delete", "noop"]:
        """Classify target-state changes after resolution, before mutation."""
        if target.state == "present":
            if row is None:
                return "create"
            current = (
                row[2],
                row[3],
                row[4],
                row[5],
                row[6],
                row[7],
                row[8],
                row[9],
                bool(row[10]),
            )
            desired = (
                target.merchant_pattern,
                target.match_type,
                target.min_amount,
                target.max_amount,
                target.account_id,
                target.category,
                target.subcategory,
                target.priority,
                True,
            )
            return "noop" if current == desired else "set"
        if row is None:
            return "noop"
        if target.state == "inactive":
            return "noop" if not bool(row[10]) else "deactivate"
        return "delete"

    def apply_rule_targets(
        self,
        plan: RuleTargetPlan,
        *,
        actor: str,
        verify: Callable[[RuleTargetPlan], None] | None = None,
    ) -> list[RuleTargetResult]:
        """Re-plan, verify live state, and apply every changed target atomically."""
        self._db.begin()
        try:
            live_plan = self.plan_rule_targets([item.target for item in plan.items])
            if verify is not None:
                verify(live_plan)
            if not live_plan.changed:
                raise UserError(
                    "Every categorization rule already has its requested state.",
                    code=error_codes.MUTATION_NOTHING_TO_DO,
                )
            results: list[RuleTargetResult] = []
            for item in live_plan.items:
                rule_id = item.rule_id
                if item.action == "create":
                    rule_id = self._create_rule_target(item.target, actor=actor)
                elif item.action == "set":
                    self._set_rule_target(item.target, rule_id, actor=actor)
                elif item.action == "deactivate":
                    self._rules.deactivate(
                        self._require_rule_id(rule_id), actor=actor, in_outer_txn=True
                    )
                elif item.action == "delete":
                    self._rules.delete(
                        self._require_rule_id(rule_id), actor=actor, in_outer_txn=True
                    )
                results.append(
                    RuleTargetResult(
                        rule_id=rule_id,
                        state=item.target.state,
                        changed=item.action != "noop",
                    )
                )
            self._db.commit()
            return results
        except BaseException:
            self._db.rollback()
            raise

    @staticmethod
    def _require_rule_id(rule_id: str | None) -> str:
        """Narrow a preflighted rule ID for the repository write boundary."""
        if rule_id is None:  # pragma: no cover - preflight establishes it
            raise RuntimeError("A resolved rule target has no rule ID")
        return rule_id

    def _create_rule_target(self, target: RuleStateTarget, *, actor: str) -> str:
        """Insert one fully validated rule target through its audited repository."""
        category = cast(str, target.category)
        event = self._rules.insert(
            name=self._rule_target_name(target),
            merchant_pattern=cast(str, target.merchant_pattern),
            match_type=cast(str, target.match_type),
            min_amount=target.min_amount,
            max_amount=target.max_amount,
            account_id=target.account_id,
            category=category,
            subcategory=target.subcategory,
            category_id=resolve_category_id(self._db, category, target.subcategory),
            priority=cast(int, target.priority),
            created_by="mcp",
            actor=actor,
            in_outer_txn=True,
        )
        return cast(str, event.target_id)

    def _set_rule_target(
        self, target: RuleStateTarget, rule_id: str | None, *, actor: str
    ) -> None:
        """Set one existing rule's complete active target through its repository."""
        category = cast(str, target.category)
        self._rules.set_target(
            self._require_rule_id(rule_id),
            name=self._rule_target_name(target),
            merchant_pattern=cast(str, target.merchant_pattern),
            match_type=cast(str, target.match_type),
            min_amount=target.min_amount,
            max_amount=target.max_amount,
            account_id=target.account_id,
            category=category,
            subcategory=target.subcategory,
            category_id=resolve_category_id(self._db, category, target.subcategory),
            priority=cast(int, target.priority),
            actor=actor,
            in_outer_txn=True,
        )

    @staticmethod
    def _rule_target_name(target: RuleStateTarget) -> str:
        """Derive the one human-readable name from the complete target fields."""
        return f"{target.match_type}: {target.merchant_pattern} → {target.category}"

    def delete_rule_categorizations(
        self, rule_id: str, *, actor: str = "system"
    ) -> None:
        """Strip categorizations a now-inactive rule had written.

        Targets only ``categorized_by IN ('rule', 'auto_rule')`` rows with
        this ``rule_id``. Writes from higher-priority sources (user /
        migration / ml / plaid) that happen to reference this rule_id are
        left intact. Run by the facade's ``deactivate_rule`` reapply tail
        immediately before ``categorize_pending`` re-evaluates the affected
        transactions against the remaining active matchers. Routes through the
        repo so each stripped row emits a paired ``category.clear`` audit
        capturing its full prior state (Invariant 10, Req 4). ``actor`` is the
        surface that triggered the deactivation (threaded from ``deactivate_rule``).
        """
        self._tx_categories.delete_by_rule(rule_id, actor=actor)

