"""Categorization writes: per-transaction edits, taxonomy, rules, exemplars.

Every write to ``app.transaction_categories``, ``app.user_merchants``,
``app.user_categories``, ``app.category_overrides``, and
``app.categorization_rules`` routes through this module. Pairs with
``CategorizationMatcher`` (read-only) under the facade.

Audit emission is scoped to the user-manual-edit path
(``set_category`` / ``clear_category``). Bulk and automated writes are
deliberately audit-silent today; broader coverage is tracked in the
app-integrity Invariant 10 work and intentionally out of scope here.

Source-precedence enforcement is centralised in :meth:`write_categorization`:
every write compares the incoming source's priority (from
``_shared.SOURCE_PRIORITY``) against the existing row's via the same
SQL CASE expression generated by ``_shared.priority_case_sql``, so the
ladder lives in exactly one place. The lone sanctioned bypass is
:meth:`set_category_in_active_txn`, which is restricted to
``categorized_by='user'`` precisely so admitting a lower-priority source
through it would be impossible. The two methods co-locate here so the
warning in the bypass's docstring stays next to the guard it bypasses.
"""

